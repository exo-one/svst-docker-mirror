#!/bin/bash

TESTFILE=docker-compose.test.yml

if [[ ! -e docker-compose.yml ]]; then
    echo "No docker-compose.yml found in $(pwd)"
fi

echo "# WARNING - Do not edit this file directly - use regen-docker-test.sh\n" > $TESTFILE

cat docker-compose.yml >> $TESTFILE

cat <<EOF >> $TESTFILE

  sut:  # tests
    build:
      context: .
      dockerfile: Dockerfile.test
EOF

echo "Generated $TESTFILE"